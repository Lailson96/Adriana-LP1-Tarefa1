public class Maior10 {
	/*
	 * 7. Ler um valor e escrever a mensagem É MAIOR QUE 10! 
	 * se o valor lido for maior que 10, caso contrário escrever NÃO É MAIOR QUE 10!

	 */
	public void main (String [] args ) {
		int numero =11;
		
	if (numero>10) {
		System.out.println("E Maior que 10! ");
	}
	else {
		System.out.println("E Menor que 10! ");
	}
	}
}
/*
Número		condicional		Saída
11			11 > 10			É MAIOR QUE 10!
*/
