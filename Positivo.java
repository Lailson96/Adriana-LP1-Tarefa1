
public class Positivo {
	/*8. Ler um valor e escrever se é positivo ou negativo 
	 * (considere o valor zero como positivo).
	 */
	public static void main (String[] args) {
		int numero = 4;
	if (numero>=0) {
		System.out.println("Positivo");
	}else {
		System.out.println("Negativo");
	}
	}
	
}
